# função sum (somar)
numeros = [1, 2, 3, 4, 5]
print(sum(numeros))

# função len (contar)
print(len(numeros))

# função max (maior)
print(max(numeros))

# função min (menor)
print(min(numeros))

# função any (verificar se algum elemento é verdadeiro)
print(any([True, False, False]))

# função all (verificar se todos os elementos são verdadeiros)
print(all([True, True, True]))

# função sorted (ordenar)
nomes = ["Lukas", "João", "Maria", "Pedro"]
print(sorted([nomes]))
print(nomes) # A lista não muda

# função join

print("|".join(nomes))


